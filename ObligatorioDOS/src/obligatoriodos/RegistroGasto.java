    private void cargarObrasALista() {
        String empty[] = {""};
        listObrasRegistradas.setListData(empty);
        String[] arrayObras = new String[sistema.getObras().size()];
        for (int i = 0; i < sistema.getObras().size(); i++) {
            arrayObras[i] = sistema.getObras().get(i).getNumeroDePermiso() + ", " + sistema.getObras().get(i).getDireccion();
        }
        listObrasRegistradas.setListData(arrayObras);
    }

    private void cargarRubrosALista() {
        String empty[] = {""};
        listRubrosRegistrados.setListData(empty);
        String[] arrayRubros = new String[sistema.getRubros().size()];
        for (int i = 0; i < sistema.getRubros().size(); i++) {
            arrayRubros[i] = sistema.getRubros().get(i).getNombre();
        }
        listRubrosRegistrados.setListData(arrayRubros);
    }

    private Rubro devolverRubroPorNombre(String nombre) {
        Rubro rubro = new Rubro(null, null, 0);
        for (int i = 0; i < sistema.getRubros().size(); i++) {
            if (nombre.equals(sistema.getRubros().get(i).getNombre())) {
                rubro = sistema.getRubros().get(i);
            }
        }
        return rubro;
    }

