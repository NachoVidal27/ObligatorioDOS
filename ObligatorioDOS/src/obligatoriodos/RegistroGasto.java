    private void btnRegistrarGastoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarGastoMouseClicked
        if (txtMonto.getText().isEmpty() || txtAreaDescripcion.getText().isEmpty() || listObrasRegistradas.getSelectedValue() == null || listRubrosRegistrados.getSelectedValue() == null) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos y seleccione un rubro y una obra.");
            return; // Salir del método si algún campo está vacío
        }
        for (int i = 0; i < sistema.getObras().size(); i++) {
            if (parseInt(listObrasRegistradas.getSelectedValue().split(",", 2)[0]) == (sistema.getObras().get(i).getNumeroDePermiso())) {
                int monto = parseInt(txtMonto.getText());
                int mes = (Integer) spnMes.getValue();
                int anio = (Integer) spnAnio.getValue();
                Gasto gasto = new Gasto(monto, mes, anio, txtAreaDescripcion.getText(), sistema.getObras().get(i).getGastos().size() + 1, devolverRubroPorNombre(listRubrosRegistrados.getSelectedValue()), false);
                sistema.getObras().get(i).setGastos(gasto);
                JOptionPane.showMessageDialog(this, "Gasto registrado a la obra con nro de permiso " + sistema.getObras().get(i).getNumeroDePermiso() + "\nNumero de gasto para esta obra: " + gasto.getNumeroDeGasto());
                txtMonto.setText("");
                txtAreaDescripcion.setText("");
            }
        }
    }//GEN-LAST:event_btnRegistrarGastoMouseClicked

